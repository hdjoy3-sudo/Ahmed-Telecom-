# গিট ইনিশিয়ালাইজ করুন
git init

# সব ফাইল ট্র্যাকিংয়ে যুক্ত করুন
git add .

# প্রথম কমিট করুন
git commit -m "Initial commit of Tushar Tech App"

# মেইন ব্রাঞ্চ সেট করুন
git branch -M main

# আপনার গিটহাব রিপোসিটরি লিংক করুন (YOUR_USERNAME এবং REPO_NAME পরিবর্তন করুন)
git remote add origin https://github.com/YOUR_USERNAME/REPO_NAME.git

# কোড গিটহাবে পুশ করুন
git push -u origin main